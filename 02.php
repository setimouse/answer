function solution($nums) {
  // f(n) = f(n - 2) + nums[n], f(n) = max({f})
  $f = [$nums[0], $nums[1]];
  for ($i = 2; $i < count($nums); $i++) {
    $k = $i % 2;
    $f[$k] += $nums[$i];
    $f[$k] = max($f);
  }
  return max($f);
}

